//__ Dizinin Metodları

/**
*   push: Dizinin sonuna bir veya daha fazla eleman ekler.
*   unshift: Dizinin başına bir veya daha fazla eleman ekler.

*   pop: Dizinin sonundaki elemanı çıkarır.
*   shift: Dizinin başındaki elemanı çıkarır.

*   splice(index,deleteCount): Dizideki belirli bir index'ten itibaren elemanları ekleyebilir veya çıkarabilir.
*   toString: Diziyi bir string'e dönüştürür.

*   join: Dizinin elemanlarını birleştirerek bir string oluşturur.
*   concat: İki veya daha fazla diziyi birleştirir.
*   slice: Belirli bir aralıktaki elemanları kopyalar ve yeni bir dizi oluşturur.
*   length: Dizinin eleman sayısını verir.
*   reverse: Dizinin elemanlarını ters çevirir.
*   split: Bir string'i belirli bir ayraçla böler ve bir diziye dönüştürür.
*   indexOf: Belirli bir elemanın dizideki ilk indeksini döndürür.
*   includes: Belirli bir elemanın dizide bulunup bulunmadığını kontrol eder.
*/